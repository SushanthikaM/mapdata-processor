package com.example.mapdata_processor.model;

public class Metadata {
    private String id;
    private String type;
    private double rating;
    private int reviews;

    public String getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public double getRating() {
        return rating;
    }

    public int getReviews() {
        return reviews;
    }
}
